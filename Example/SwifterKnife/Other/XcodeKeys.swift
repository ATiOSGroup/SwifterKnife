 1. 快速自动缩进
 当你的代码没有对齐时，这个快捷键非常有用。

 control + i / ⌃ + i
 
 2. 重命名变量名
 command + control + e / ⌘ + ⌃ + e
 
 4. 查找变量名
 下一个
 option + control + e / ⌥ + ⌃ + e
 
 上一个
 shift + option + control + e / ⇧ + ⌥ + ⌃ + e
 
 5. 代码块向上或向下移动
 向上移动：
 option + command + [ / ⌥ + ⌘ + [

 向下移动：
 option + command + ] / ⌥ + ⌘ + ]
 
 6. 多行光标（使用鼠标）
 有时你需要在文件的不同部分中写入相同的内容
 shift + control + click / ⇧ + ⌃ + click
 
 7. 多行光标（使用键盘）
 shift + control + up or down /⇧ + ⌃ + ↑ or ↓
 
 8. 快速创建带有多个参数的初始化（init）函数
 control + option + command + i
 
 9. 返回光标之前所在的位置
 option + command + L / ⌥ + ⌘ + L
